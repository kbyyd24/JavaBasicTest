package ExtendsAndPackage.PackageWithSuperclass;

public class Score {
	int Chinese;
	int Math;
	int English;
	
	public void setChinese(int chinese) {
		Chinese = chinese;
	}
	public int getChinese() {
		return Chinese;
	}
	public void setEnglish(int english) {
		English = english;
	}
	public int getEnglish() {
		return English;
	}
	public void setMath(int math) {
		Math = math;
	}
	public int getMath() {
		return Math;
	}
}
